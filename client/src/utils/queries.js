// We've used similar syntax from the test query we wrote using the GraphQL Playground earlier. 
//Now we've wrapped the entire query code in a tagged template literal using the imported gql function. 
// We've also saved it as QUERY_THOUGHTS and exported it using the ES6 module export syntax.

// we can import this function anywhere we need throughout the frontend 
import { gql } from '@apollo/client';

export const QUERY_THOUGHTS = gql`
  query thoughts($username: String) {
    thoughts(username: $username) {
      _id
      thoughtText
      createdAt
      username
      reactionCount
      reactions {
        _id
        createdAt
        username
        reactionBody
      }
    }
  }
`;

export const QUERY_THOUGHT = gql`
  query thought($id: ID!) {
    thought(_id: $id) {
      _id
      thoughtText
      createdAt
      username
      reactionCount
      reactions {
        _id
        createdAt
        username
        reactionBody
      }
    }
  }
`;

export const QUERY_USER = gql`
  query user($username: String!) {
    user(username: $username) {
      _id
      username
      email
      friendCount
      friends {
        _id
        username
      }
      thoughts {
        _id
        thoughtText
        createdAt
        reactionCount
      }
    }
  }
`;

// With this query, we're going to retrieve essentially all data related to the logged-in user. 
// We'll retrieve their user information, thoughts, reactions to those thoughts, and friend list
// great for users personal profile page 
